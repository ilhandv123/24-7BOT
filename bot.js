// ūü§Ė BOT SYSTEM
function startBot(ip, port, username) {

  function createBot() {

    console.log(chalk.blue(`ūüĒĄ Trying as ${username}...\n`))

    const bot = mineflayer.createBot({
      host: ip,
      port: port,
      username: username,
      version: '1.20.1'
    })

    let connected = false
    let wasKicked = false
    let isRestarting = false

    bot.on('spawn', () => {
      connected = true
      console.log(chalk.green(`‚úÖ Joined as ${username}\n`))

      // ūüí¨ Chat loop
      const messages = ['ok', 'hello', 'hai', 'anyone here?', 'lets play']
      function chatLoop() {
        setTimeout(() => {
          const msg = messages[Math.floor(Math.random() * messages.length)]
          bot.chat(msg)
          console.log(chalk.magenta(`ūüí¨ ${msg}`))
          chatLoop()
        }, 5 * 60 * 1000)
      }
      chatLoop()

      // ūü¶ė Jump
      setInterval(() => {
        bot.setControlState('jump', true)
        setTimeout(() => bot.setControlState('jump', false), 800)
      }, 3 * 60 * 1000)

      // ūüö∂ Move
      setInterval(() => {
        bot.setControlState('forward', true)
        setTimeout(() => bot.setControlState('forward', false), 3000)
      }, 4 * 60 * 1000)

      // ūüĒĀ Restart
      setTimeout(() => {
        console.log('ūüĒĀ Restarting...')
        isRestarting = true
        bot.quit()
      }, 60 * 60 * 1000)
    })

    bot.on('kicked', () => wasKicked = true)

    bot.on('end', () => {
      console.log('ūüĒĀ Reconnecting in 20s...')

      if (wasKicked && !isRestarting) {
        username = randomName()
        console.log(`ūüĒĄ New username: ${username}`)
      }

      wasKicked = false
      isRestarting = false

      setTimeout(createBot, 20000)
    })
  }

  createBot()
      }
